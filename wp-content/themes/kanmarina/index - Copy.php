                    <div class="post-entry">

                        <p>С трудом верится, но я веду эту рубрику ровно год! Ровно год стараюсь фиксировать события каждого месяца и собираю их в длинную простыню из картинок и слов. По статистике эта рубрика не самая популярная (зато самая регулярная), но вы уж простите меня, те, кто не любит про жизнь, я продолжу это делать и советую каждому задуматься о чём-то подобном. Могу поспорить, просто так вести записи и раскладывать фото по папкам вы ленитесь!</p>
