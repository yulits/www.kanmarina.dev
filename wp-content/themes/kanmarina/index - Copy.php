                    <div class="post-entry">

                        <p>Вчера я отправила очередное Теплое письмо. Так я называю рассылку, потому что это не настоящая рассылка про новые посты в блоге, это искренние письма о том, что происходит за кадром, что волнует меня, это доверительная беседа с теми, кто в ней нуждается. Ну, и новые посты в блоге тоже :)</p>
                        <p>Так вот, это письмо было про инсайты 2016. Возможно, я немножко порекламировала это письмо и его тему на Facebook, но не все успели подписаться и получить его. Поэтому сегодня мне написало уже несколько человек с просьбами отправить то письмо.</p>
                        <p>Ну и правда, чего я от вас буду скрывать? Разочек в виде исключения нарушим эксклюзивность Теплых писем. А на будущее подписывайтесь, друзья, я точно не спамер, на самом деле, я даже слишком редко пишу, но делаю это от души и всегда получаю не менее теплую обратную связь.</p>
