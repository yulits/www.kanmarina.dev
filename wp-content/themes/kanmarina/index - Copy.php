                        <h2><a href="http://marinagiller.com/2017/02/prosto-takoi-yanvar.html/">Просто такой январь</a></h2>

                    </div>

                    <div class="post-entry">

                        <p>Январь был очень тихим и весь спрятался внутри. На фото это тоже сказалось. Мы тут переживаем аномально холодную зиму, в доме у нас бывает до +10 при влажности 80%, а это, друзья мои, то ещё удовольствие! Но знаете, в холоде мои мозги работают гораздо лучше (и ещё, говорят, для сохранения молодости полезно).</p>
                        <p>Так что ресурс пущен в дело — ковыряюсь в себе и в своей голове, наконец, наводя порядок, который я так болезненно искала целый год. Знаете, когда ко мне на консультации приходят клиенты, часто за 15 минут удается увидеть общую картину и подобрать ключик, сказать правильные слова и дать человеку ресурс для перемен. Моя работа, в общем, не в этом, но получается и такое. А на себя так посмотреть, конечно, не удается. Я иногда даже играю в игру «Марина на консультации у Марины». Но нет, не работает. Я всё ещё ищу своего ментора, с которым кликнет. А пока потихоньку ковыряю себя сама.</p>
                        <p>И вот я, наконец, почувствовала, что кое-что наковыряла. Мыслекаша из моей головы стала потихоньку выходить на бумагу в виде списков и схем, достаточно прозрачных, чтобы по ним можно было работать. И я вздрагиваю от предвкушения качественного скачка, которое иногда удается почувствовать на кончиках пальцев. Только бы не спугнуть!</p>
                        <p>И фото тоже не про факты, а про состояния. Новостями буду сыпать, когда придет весна. Во всех смыслах этого слова.</p>
