                        <h2><a href="http://marinagiller.com/2017/02/kak-naiti-sebya.html/">История о том, как я обманула саму себя</a></h2>

                    </div>

                    <div class="post-entry">

                        <p>Накануне Дня рождения я традиционно прячусь от мира и ковыряюсь в своей голове. Но впервые за долгие годы я встретила этот день в прекрасном расположении духа, потому что ответы на экзистенциальные вопросы обогнали график и пришли заранее. И раз уж мне теперь 33, я взрослая и мудрая, хочу поделиться с вами тем, что происходило со мной в последние полгода, куда это меня завело и самое главное — как я из этого вылезла.</p>
                        <p>Быть собой и делать то, во что веришь. Сколько раз вы слышали эту фразу? Сколько из них — от меня? Но, как и многие важные и с виду простые вещи, эта штука — настоящий сундук с двойным дном. Вот, кажется, ты верен себе до последней капли, а однажды просыпаешься и бум! Становится абсолютно ясно, что ты так далеко от себя, как будто всё это время шел в противоположную сторону.</p>
                        <p>Слышали про то, как мы совершаем покупки? Свой изначально импульсивный выбор мы впоследствии рационализируем, находя вымышленную логику в своих решениях. Точно так же, находясь в любой точке пути и оглядываясь назад, можно легко объяснить, как именно ты тут оказался, почему каждый шаг был верным. А на самом деле ты просто метался, как тот <a href="https://tjournal.ru/40541-mem-sherstyanoi-volchara" target="_blank">шерстяной волчара</a>, всё дальше отклоняясь от себя настоящего.</p>
                        <p>Что произошло со мной и почему это важно? Может быть, мои конкретные примеры вам не близки, но думаю, аналогии провести будет не сложно. Вот где я обнаружила себя к своему собственному изумлению, как человек, который страдает лунатизмом и внезапно очнулся посреди ночи на крыше, вот-вот готовый спрыгнуть вниз.</p>
